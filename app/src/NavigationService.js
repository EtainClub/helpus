import { NavigationActions } from 'react-navigation';

let _navigator;

function setTopLevelNavigator(navigatorRef) {
  _navigator = navigatorRef;
}

function navigate(routeName, params) {
  // @todo when the active screen is already helpscreen, then how to refresh the screen with new data?
//  if (__DEV__) console.log('navigator', _navigator);
  
  _navigator.dispatch(
    NavigationActions.navigate({
      routeName,
      params,
    })
  );
}

